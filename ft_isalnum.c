#include "libft.h"

int	ft_isalnum(int i)
{
	if ((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z'))
	{
		i++;
	}
	else if (i >= '0' && i <= '9')
	{
		i++;
	}
	else
	{
		return (0);
	}
	return (1);
}
